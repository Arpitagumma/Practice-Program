public class ReturnEvenNumbers {
    public static void main(String[] args) {
        int[] numbers={12,4,2,1,53,64,14,54,33};
        System.out.println("even numbers in array");
        for(int num:numbers){


       if(num%2==0){
           System.out.println(num+"");
        }
       }


    }

}
